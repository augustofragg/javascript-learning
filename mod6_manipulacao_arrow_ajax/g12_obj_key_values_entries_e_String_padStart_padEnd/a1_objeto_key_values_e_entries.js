//Object keys, values e entries

//Exemplo 1

let lista = ['ovo','macarrao','feijao','pipoca'];

console.log("Exemplo 1")

console.log("Object keys: " + Object.keys(lista));

console.log("Object values: " + Object.values(lista));

console.log("Object entries: " + Object.entries(lista));

//Exemplo 2

let person = {
    name: 'Carlos Augusto',
    lastName: 'Fragoso Bastos',
    age: 90
}

console.log("Exemplo 2");

console.log("Object keys: " + Object.keys(person));

console.log("Object values: " + Object.values(person));

console.log("Object entries: " + Object.entries(person));